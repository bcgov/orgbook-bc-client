<template>
  <div>
    <div class="search-bar">
      <v-container :fluid="$vuetify.breakpoint.smAndDown" class="pa-5">
        <SearchBar :query="q"></SearchBar>
      </v-container>
    </div>
    <v-container :fluid="$vuetify.breakpoint.smAndDown" class="pa-5 pt-0 pb-0">
      <SearchResult
        v-if="pagedSearchTopics.total"
        :results="pagedSearchTopics"
      />
    </v-container>
  </div>
</template>

<script lang="ts">
import { Component, Vue, Watch } from "vue-property-decorator";
import { mapActions, mapGetters } from "vuex";
import { ISearchQuery } from "@/interfaces/api/v4/search-topic.interface";
import { defaultQuery } from "@/utils/search";
import SearchBar from "@/components/search/SearchBar.vue";
import SearchResult from "@/components/search/SearchResult.vue";
import SearchComponent from "@/components/search/SearchComponent.vue";

interface Data {
  q: string | null;
}

@Component({
  components: {
    SearchBar,
    SearchResult,
    SearchComponent
  },
  computed: {
    ...mapGetters(["searchQuery", "pagedSearchTopics"]),
  },
  methods: {
    ...mapActions([
      "setLoading",
      "setSearchQuery",
      "setSearchFilters",
      "fetchSearchFacetedTopics",
    ]),
  },
})
export default class Home extends Vue {
  q!: unknown;

  setLoading!: (loading: boolean) => void;
  setSearchQuery!: (query: ISearchQuery) => void;
  setSearchFilters!: (query: ISearchQuery) => void;
  fetchSearchFacetedTopics!: () => void;

  data(): Data {
    return {
      q: null,
    };
  }

  created(): void {
    const query = this.$route.query as unknown as ISearchQuery;
    if (query?.q) {
      this.q = query?.q || null;
      const newQuery = { ...defaultQuery, ...query };
      this.setSearchFilters(newQuery);
      this.setSearchQuery(newQuery);
    }
  }

  @Watch("searchQuery")
  async onSearchQuery(
    newQuery: ISearchQuery,
    oldQuery: ISearchQuery
  ): Promise<void> {
    if (newQuery?.q && JSON.stringify(newQuery) !== JSON.stringify(oldQuery)) {
      this.setLoading(true);
      await this.fetchSearchFacetedTopics();
      this.setLoading(false);
    }
  }
}
</script>

<style scoped>
.search-bar {
  color: white;
  background: #b3b3b3;
}
</style>
